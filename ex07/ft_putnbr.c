#include  <unistd.h>

void	ft_putnbr(int nb)
{
	int	arr[11];
	int	i;

	i = 0;
	if (nb < 0)
	{
		write (1, "-", 1);
		nb *= -1;
	}
	while (nb > 0)
	{
		arr[i] = nb % 10 + 48;
		nb /= 10;
		i++;
	}
	while (i >= 0)
	{
		write(1, &arr[i], 1);
		i--;
	}
}
